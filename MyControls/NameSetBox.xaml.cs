    /// <summary>
    /// NameSetBox.xaml 的交互逻辑
    /// </summary>
    public partial class NameSetBox : UserControl
    {
        public NameSetBox()
        {
            InitializeComponent();
        }



        public int Number
        {
            get { return (int)GetValue(NumberProperty); }
            set { SetValue(NumberProperty, value); }
        }

        // Using a DependencyProperty as the backing store for Number.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty NumberProperty =
            DependencyProperty.Register("Number", typeof(int), typeof(NameSetBox), new PropertyMetadata(1));



        public string Text
        {
            get { return (string)GetValue(TextProperty); }
            set { SetValue(TextProperty, value); }
        }

        // Using a DependencyProperty as the backing store for Text.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty TextProperty =
            DependencyProperty.Register("Text", typeof(string), typeof(NameSetBox), new PropertyMetadata(string.Empty));


    }
